# House Price Prediction Using Machine Learning

#import all the necessary libraries
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

# Load the datasets into df
df=pd.read_csv('/content/housing.csv')
df.head()

df.info()

df=df.dropna()

df=df.drop_duplicates()

# Sort by every column in the order they exist in the DataFrame
df = df.sort_values(by=list(df.columns))

df.head()

df.info()

# Identify x and y which is independant and dependant variable
x=df.iloc[:,:-2]
y=df.iloc[:,-2]

#Apply the train test split to split the data
from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y,random_state=42,test_size=0.25)

# Now choose the model to predict the price of the house
from sklearn.linear_model import LinearRegression

# Now Define the Model and train it
# x_train_reshaped = x_train.reshape(-1, 1)
LinearModel=LinearRegression()
LinearModel.fit(x_train,y_train)

y_pred=LinearModel.predict(x_test)

y_pred

from sklearn.metrics import r2_score
print("R2_score is: ",r2_score(y_test,y_pred)*100,"%")



plt.plot(x_train['total_bedrooms'],y_train,'g.')
plt.plot(x_test['total_bedrooms'].head(),y_pred[:5],'b')

